    <h2>Overview</h2>
    <p>
      Credit cards are issued by a financial institution and supported by a card
      processing network such as Visa, Mastercard or American Express which are
      the most common in New Zealand. Debit cards use the exact same networks
      except they are directly linked to your bank account so as soon as you
      make a purchase, it is deducted from your account. Credit cards are a form
      of unsecured debt that must be paid back after a certain interest free
      period such as 55 days. Because there are many intermediary parties
      involved, credit cards are more costly to process payments through than
      say debit cards. The order of cost goes cash, EFTPOS, debit cards, credit
      cards.
    </p>
  </section>
  <section>
    <h2>Rules for Using Credit Cards</h2>
    <p>
      Basically, if you are going to use a credit card, you must always pay the
      full balance off every month to avoid any interest or fees. As soon as you
      miss a payment you should cut up the card, pay it off and never touch them
      again. Because of this fact, the various interest rates for purchases,
      cash advances and balance transfers are actually irrelevant. Additionally,
      if the card has an annual fee, you must spend a minimum amount per year on
      the card for the rewards to eclipse the cost of the annual fee, if you
      expect to easily achieve this every year, stay away.
    </p>
  </section>
  <section>
    <h2>Rewards and Benefits</h2>
    <p>
      So the only reason anyone would use a credit card is if they can get some
      kind of reward or benefit from it. This comes in many forms but can
      essentially be boiled down to some rate of "cash back". Usually, this rate
      is no more than 2% at the absolute highest in New Zealand. When you think
      about it, that's like getting a meagre 2% discount on everything you
      spend. If you spend $20,000 per year on the card that means you're likely
      to get back just $400, or another way of thinking about it is like getting
      a $20 voucher for every $1000 you spend.
    </p>
    <p>
      For example, a card might offer you $1 cash back for every $150 you spend,
      that's a 0.67% cashback rate. If the card has a $50 annual fee, you would
      need to spend $7462.67 to break even. That same $20,000 total spending
      gets you $84 for the year after paying the annual fee. Another thing to
      consider is that if you increase your spending by just 0.42% in this case
      due to it being on a credit card, you would not have gained anything.
    </p>
    <p>
      Another type of reward is automatic travel insurance when you make a
      qualifying purchase such as buying plane tickets or booking a hotel. This
      could be worth between $400 and $1000 per year depending on how much you
      travel. If we take this alone and apply it to that same $20,000 of
      spending it works out to be between a 1.5 and 5% cash back rate. Although
      some years you might not travel and this will bring down the average. So
      clearly these additional benefits are much more significant than the
      reward points or cash back.
    </p>
