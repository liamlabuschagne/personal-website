      Each of these types of accounts can be offered as a PIE (Portfolio
      Investment Entity) which is a tax structure originally created for
      KiwiSaver that essentially caps your tax rate at 28% (which is the company
      tax rate) so if you are in a higher personal income tax bracket you should
      seriously consider accounts with this tax structure.
