    <h2>Types</h2>
    <p>
      The only difference between savings accounts are the interest rate you get
      and how restricted you are to withdrawing the funds. Generally, the more
      restrictive the account is, the more interest it will pay you for that
      commitment. This is because banks can then more easily plan on having a
      certain level of deposits.
    </p>
    <p>These are the three types of savings accounts:</p>
    <ul>
      <li>On-call</li>
      <li>Notice</li>
      <li>Term Deposit</li>
    </ul>
    <p>
      On-call means that you can withdraw the money at any time without notice.
      Some on-call savings accounts will have a bonus interest rate if you meet
      certain requirements like increasing the balance by $50 per month or not
      making any withdrawals. Sometimes banks will even charge you fees if you
      make too many withdrawals.
    </p>
    <p>
      Notice savings accounts are exactly the same but require to give a certain
      amount of notice like 30 or 90 days which practically means you initiate a
      withdrawal and choose a date for that withdrawal at least that far into
      the future. In the mean time you can continue to deposit money into it as
      normal and you will earn whatever the current interest rate is.
    </p>
    <p>
      A term deposit is the most restrictive where you actually lock in both the
      term and the interest rate. Because of this they generally have the
      highest interest rates available.
