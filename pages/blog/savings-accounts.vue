    <h2>How much should I keep in savings?</h2>

    <p>
      Firstly, I'll say that as a general rule because the risk of savings
      accounts are very low (the value of your deposit doesn't change) you will
      on average be getting a lower return on your money than some kind of
      investment (who's value does fluctuate) because you are taking on more
      risk. This means you should actually try to minimise your how much you
      keep in "cash" (which are physical cash and bank deposits) because you are
      paying an opportunity cost as well as inflation eating away at the value
      of your money.
    </p>
    <p>
      Infact, interest rates are generally lower than the rate of inflation
      because central banks use this to stimulate or cool down the economy to
      keep inflation within an acceptable range. So you could argue that money
      kept in a savings account will on average always be loosing money to
      inflation and therefore should not be thought of as an investment.
    </p>
    <p>
      Therefore, your cash position should only be just enough to cover your day
      to day spending between paydays and some extra for unplanned expenses that
      may come up. Beyond this, you should only have more in savings if you are
      looking to use that money within a few years for say a house deposit. The
      rest should be invested to get a higher return.
    </p>
    <p>
      The only exception to this rule may be if you are entering retirement and
      are more interested in creating an income portfolio but again that is not
      the focus of this article and if you are unsure of anything you should
      seek financial advice for all matters especially retirement planning.
    </p>
